namespace FractalDataWorks.SmartGenerators.CodeBuilders.Documentation;

/// <summary>
/// Defines the access modifiers for class members and types.
/// </summary>
public enum AccessModifier
{
    /// <summary>
    /// No access modifier specified.
    /// </summary>
    None,

    /// <summary>
    /// The public access modifier.
    /// </summary>
    Public,

    /// <summary>
    /// The private access modifier.
    /// </summary>
    Private,

    /// <summary>
    /// The protected access modifier.
    /// </summary>
    Protected,

    /// <summary>
    /// The internal access modifier.
    /// </summary>
    Internal,

    /// <summary>
    /// The protected internal access modifier.
    /// </summary>
    ProtectedInternal,

    /// <summary>
    /// The private protected access modifier.
    /// </summary>
    PrivateProtected,
}
